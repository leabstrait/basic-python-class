weekday_num = input("Please enter a number")

weekday_num = int(weekday_num)

if weekday_num == 1:
    print("Sunday")
elif weekday_num == 2:
    print("Monday")
elif weekday_num == 3:
    print("Tuesday")
elif weekday_num == 4:
    print("Wednesday")
elif weekday_num == 5:
    print("Thursday")
elif weekday_num == 6:
    print("Friday")
elif weekday_num == 7:
    print("Saturday")
else:
    print("Not a weekday number")